#include "3-calc.h"

/**
 *op_add - add
 *@a: num1
 *@b: num2
 *Return: a+b
 */
int op_add(int a, int b)
{
return (a + b);	
}

/**
 *op_sub - subtact
 *@a: num1
 *@b: num2
 *Return: a - b
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 *op_mul - multiply
 *@a: num1
 *@b: num2
 *Return: a * b
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
*op_div
*@a: num1
*@b: num2
*Return: division a/b
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
 *op_mod - module
 *@a: num1
 *@b: num2
 *Return: module a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
